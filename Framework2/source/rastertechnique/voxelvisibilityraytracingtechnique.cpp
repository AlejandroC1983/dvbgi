/////////////////////////////////////////////////////////////////////////////////////////////

void VoxelVisibilityRayTracingTechnique::buildShaderBindingTable()
{
	MaterialVoxelVisibilityRayTracing* temp = static_cast<MaterialVoxelVisibilityRayTracing*>(m_vectorMaterial[0]);

	vector<VkRayTracingShaderGroupCreateInfoKHR>& vectorRayTracingShaderGroupCreateInfo = m_materialVoxelVisibilityRayTracing->refShader()->refVectorRayTracingShaderGroupCreateInfo();

	uint numberShaderGroup      = static_cast<uint>(vectorRayTracingShaderGroupCreateInfo.size());
	uint shaderBindingTableSize = numberShaderGroup * m_shaderGroupSizeAligned;

	// Retrieve shader handles
	vector<uint8_t> vectorShaderHandleStorage(shaderBindingTableSize);
	VkResult result = vkfpM->vkGetRayTracingShaderGroupHandlesKHR(coreM->getLogicalDevice(), m_materialVoxelVisibilityRayTracing->getPipeline()->getPipeline(), 0, numberShaderGroup, shaderBindingTableSize, vectorShaderHandleStorage.data());

	assert(result == VK_SUCCESS);

	vector<uint8_t> vectorShaderBindingTableBuffer(shaderBindingTableSize);
	uint8_t* pVectorShaderBindingTableBuffer = static_cast<uint8_t*>(vectorShaderBindingTableBuffer.data());

	forI(numberShaderGroup)
	{
		memcpy(pVectorShaderBindingTableBuffer, vectorShaderHandleStorage.data() + i * m_shaderGroupHandleSize, m_shaderGroupHandleSize);
		pVectorShaderBindingTableBuffer += m_shaderGroupSizeAligned;
	}

	m_shaderBindingTableBuffer = bufferM->buildBuffer(move(string("shaderBindingTable")),
		(void*)vectorShaderBindingTableBuffer.data(),
		sizeof(uint8_t) * vectorShaderBindingTableBuffer.size(),
		VK_BUFFER_USAGE_TRANSFER_SRC_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT | VK_BUFFER_USAGE_SHADER_DEVICE_ADDRESS_BIT | VK_BUFFER_USAGE_SHADER_BINDING_TABLE_BIT_KHR,
		VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
}

/////////////////////////////////////////////////////////////////////////////////////////////

void VoxelVisibilityRayTracingTechnique::slotPrefixSumComplete()
{
	m_numOccupiedVoxel    = m_techniquePrefixSum->getFirstIndexOccupiedElement();
	m_numElementToProcess = m_numOccupiedVoxel * m_numRaysPerVoxelFace * 6; // Each local workgroup will work one side of each voxel in the scene
	m_sceneMin.w          = float(m_numOccupiedVoxel);

	MaterialVoxelVisibilityRayTracing* temp = static_cast<MaterialVoxelVisibilityRayTracing*>(m_vectorMaterial[0]);

	m_materialVoxelVisibilityRayTracing->setSceneMinAndNumberVoxel(m_sceneMin);

	vector<uint> vectorData;
	vectorData.resize(m_numElementToProcess);
	memset(vectorData.data(), maxValue, vectorData.size() * size_t(sizeof(uint)));
	bufferM->resize(m_voxelVisibility4BytesBuffer, vectorData.data(), m_numElementToProcess * sizeof(uint));

	// TODO: Avoid this resize to save memory once debug is done
	bufferM->resize(m_voxelVisibilityDebugBuffer, nullptr, 1450000 * sizeof(uint));

	m_prefixSumCompleted = true;
}

/////////////////////////////////////////////////////////////////////////////////////////////

void VoxelVisibilityRayTracingTechnique::slotVoxelMergeComplete()
{
	if (m_prefixSumCompleted)
	{
		m_vectorCommand.clear();
		m_active = true;
	}
}

	// Total size = 714 + 1288 + 3994 = 5996
	for (int i = 0; i < 6; ++i)
	{
		for (int j = 0; j < 64; ++j)
		{
			vectorData.push_back(arrayTriangleNumberSampleLOD0[i][j]);
		}
	}

	for (int i = 0; i < 6; ++i)
	{
		for (int j = 0; j < 64; ++j)
		{
			vectorData.push_back(arrayTriangleSampleOffsetLOD0[i][j]);
		}
	}

	for (int i = 0; i < 6; ++i)
	{
		for (int j = 0; j < 1933; ++j)
		{
			vectorData.push_back(arrayTriangleSampleBarycentricLOD0[j].x);
			vectorData.push_back(arrayTriangleSampleBarycentricLOD0[j].y);
		}
	}

	vec2 data0 = arrayTriangleSampleBarycentricLOD0[1524];
	vec2 data1 = arrayTriangleSampleBarycentricLOD0[1525];
	vec2 data2 = arrayTriangleSampleBarycentricLOD0[1526];
	vec2 data3 = arrayTriangleSampleBarycentricLOD0[1527];
	vec2 data4 = arrayTriangleSampleBarycentricLOD0[1528];
	vec2 data5 = arrayTriangleSampleBarycentricLOD0[1529];

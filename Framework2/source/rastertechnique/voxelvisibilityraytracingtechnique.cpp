	cout << "data3=(" << data3.x << ", " << data3.y << ")" << endl;
	cout << "data4=(" << data4.x << ", " << data4.y << ")" << endl;
	cout << "data5=(" << data5.x << ", " << data5.y << ")" << endl;
	int a = 0;
